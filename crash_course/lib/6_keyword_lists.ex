# keyword lists are a way of passing options to functions.
# They are defined using a list of tuples, where each tuple is a key-value pair.
# For example: [key: value, key2: value2]
# Keyword lists are often used in Elixir for configuration and options.

# verbose way
# function(arg1, [{:key, value}])

# less verbose way
# function(arg1, key: value)

# common way
# function(arg1, key: value)
